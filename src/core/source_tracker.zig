const testing = std.testing;

test "SourceProvenance - init and deinit" {
    const p = try SourceProvenance.init(testing.allocator, .file, "src/main.zig");
    defer {
        var m = p;
        m.deinit();
    }
    try testing.expectEqual(SourceProvenance.SourceType.file, p.source_type);
    try testing.expectEqualStrings("src/main.zig", p.origin);
    try testing.expect(p.confidence == 1.0);
}

test "SourceProvenance - withConfidence clamps" {
    var p = try SourceProvenance.init(testing.allocator, .web, "https://example.com");
    defer p.deinit();
    _ = p.withConfidence(1.5);
    try testing.expect(p.confidence == 1.0);
    _ = p.withConfidence(-0.5);
    try testing.expect(p.confidence == 0.0);
}

test "SourceTracker - record and find" {
    var tracker = SourceTracker.init(testing.allocator);
    defer tracker.deinit();

    const p = try SourceProvenance.init(testing.allocator, .file, "test.zig");
    try tracker.record("k1", "Test knowledge", p);

    const found = tracker.findById("k1");
    try testing.expect(found != null);
    try testing.expectEqualStrings("Test knowledge", found.?.content);
}

test "SourceTracker - provenanceReport" {
    var tracker = SourceTracker.init(testing.allocator);
    defer tracker.deinit();

    const p1 = try SourceProvenance.init(testing.allocator, .file, "a.zig");
    try tracker.record("k1", "From file", p1);

    const p2 = try SourceProvenance.init(testing.allocator, .ai_generated, "llama3");
    try tracker.record("k2", "From AI", p2);

    const report = tracker.provenanceReport();
    try testing.expectEqual(@as(u32, 2), report.total_entries);
    try testing.expectEqual(@as(u32, 1), report.file_sources);
    try testing.expectEqual(@as(u32, 1), report.ai_sources);
}
